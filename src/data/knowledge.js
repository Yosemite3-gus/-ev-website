export function searchKnowledge(query) {
  if (!query) return []
  const lower = query.toLowerCase()
  const keywords = ['续航', '充电', '电池', '预算', '补贴', '智驾', '家用', '冬季', '配置', '保养', '保险', '对比', '选购', 'SUV', '轿车']
  const scored = []
  for (const article of articles) {
    let score = 0
    const text = article.title + article.category + article.content
    for (const kw of keywords) {
      if (lower.includes(kw) && text.includes(kw)) score++
    }
    if (lower.includes(article.category)) score += 2
    if (score > 0) scored.push({ score, article })
  }
  scored.sort((a, b) => b.score - a.score)
  return scored.slice(0, 3).map((s) => s.article)
}
